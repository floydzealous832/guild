func renderMCP() (string, error) {
	srv, err := mcp.BuildForDocs()
	if err != nil {
		return "", fmt.Errorf("build server: %w", err)
	}
	tools, err := listTools(srv)
	if err != nil {
		return "", fmt.Errorf("list tools: %w", err)
	}
	sort.Slice(tools, func(i, j int) bool { return tools[i].Name < tools[j].Name })

	var b strings.Builder
	b.WriteString(autogenBanner)
	b.WriteString("\n\n# guild MCP tool catalog\n\n")
	fmt.Fprintf(&b, "Every tool a registered MCP client sees. %d tools.\n\n", len(tools))

	b.WriteString("## Tools\n\n")
	for _, t := range tools {
		fmt.Fprintf(&b, "- [`%s`](#%s)", t.Name, t.Name)
		if t.Description != "" {
			fmt.Fprintf(&b, " — %s", firstSentence(t.Description))
		}
		b.WriteString("\n")
	}
	b.WriteString("\n")

	for _, t := range tools {
		renderTool(&b, t)
	}
	return b.String(), nil
}

func listTools(s *sdkmcp.Server) ([]*sdkmcp.Tool, error) {
	ctx := context.Background()
	clientT, serverT := sdkmcp.NewInMemoryTransports()
	serverSess, err := s.Connect(ctx, serverT, nil)
	if err != nil {
		return nil, err
	}
	defer func() { _ = serverSess.Wait() }()

	client := sdkmcp.NewClient(&sdkmcp.Implementation{Name: "guild-docgen"}, nil)
	sess, err := client.Connect(ctx, clientT, nil)
	if err != nil {
		return nil, err
	}
	defer func() { _ = sess.Close() }()

	res, err := sess.ListTools(ctx, &sdkmcp.ListToolsParams{})
	if err != nil {
		return nil, err
	}
	return res.Tools, nil
}

func renderTool(b *strings.Builder, t *sdkmcp.Tool) {
	fmt.Fprintf(b, "## `%s`\n\n", t.Name)
	if t.Description != "" {
		fmt.Fprintf(b, "%s\n\n", t.Description)
	}
	schema, _ := t.InputSchema.(*jsonschema.Schema)
	if schema == nil || len(schema.Properties) == 0 {
		b.WriteString("_no arguments_\n\n")
		renderRawSchema(b, t.InputSchema)
		return
	}
	// Render arg table from the schema.
	b.WriteString("**Arguments**\n\n")
	b.WriteString("| name | type | required | description |\n")
	b.WriteString("| --- | --- | --- | --- |\n")
	required := map[string]bool{}
	for _, r := range schema.Required {
		required[r] = true
	}
	names := make([]string, 0, len(schema.Properties))
	for n := range schema.Properties {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, n := range names {
		prop := schema.Properties[n]
		typ := prop.Type
		if typ == "" && len(prop.Types) > 0 {
			typ = strings.Join(prop.Types, "|")
		}
		if typ == "" {
			typ = "any"
		}
		req := ""
		if required[n] {
			req = "✓"
		}
		fmt.Fprintf(b, "| `%s` | %s | %s | %s |\n", n, typ, req, oneLine(prop.Description))
	}
	b.WriteString("\n")
	renderRawSchema(b, t.InputSchema)
}

func renderRawSchema(b *strings.Builder, schema any) {
	schemaJSON, err := json.MarshalIndent(schema, "", "  ")
	if err != nil {
		return
	}
	b.WriteString("<details><summary>Raw JSON schema</summary>\n\n```json\n")
	b.Write(schemaJSON)
	b.WriteString("\n```\n\n</details>\n\n")
}

