func renderReadme() string {
	return autogenBanner + `

# guild — auto-generated reference

The files in this directory are regenerated from the live code by
` + "`cmd/docgen`" + `. Do not edit them by hand; edits are overwritten
on the next ` + "`make docs`" + ` run.

| file | contents |
| --- | --- |
| [` + "`cli.md`" + `](./cli.md) | ` + "`guild`" + ` CLI reference — every subcommand with flags + usage |
| [` + "`mcp.md`" + `](./mcp.md) | MCP tool catalog — every tool with arg schema |

To regenerate:

` + "```" + `
make docs
` + "```" + `

CI catches drift via ` + "`make docs-check`" + ` (exits non-zero if the
generated output differs from what's checked in).
`
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

func writeFiles(dir string, files map[string]string) error {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	for name, body := range files {
		path := filepath.Join(dir, name)
		if err := os.WriteFile(path, []byte(body), 0o644); err != nil { //nolint:gosec // generated docs are world-readable by design
			return fmt.Errorf("write %s: %w", path, err)
		}
	}
	return nil
}

func checkFiles(dir string, files map[string]string) []string {
	var diffs []string
	for name, body := range files {
		path := filepath.Join(dir, name)
		onDisk, err := os.ReadFile(path)
		if err != nil {
			diffs = append(diffs, fmt.Sprintf("missing: %s", path))
			continue
		}
		if !bytes.Equal(onDisk, []byte(body)) {
			diffs = append(diffs, fmt.Sprintf("stale: %s", path))
		}
	}
	return diffs
}

func oneLine(s string) string {
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "|", "\\|")
	return strings.TrimSpace(s)
}

func firstSentence(s string) string {
	s = oneLine(s)
	if i := strings.Index(s, ". "); i > 0 {
		return s[:i+1]
	}
	return s
}

func fatal(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "docgen: "+format+"\n", args...)
	os.Exit(1)
}
