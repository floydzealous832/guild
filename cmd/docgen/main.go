// Command docgen regenerates the auto-produced guild reference docs
// under docs/generated/. It is the single source of truth pipeline:
//
//	spec / cobra tree  →  docgen  →  docs/generated/{cli,mcp}.md
//
// Run via `make docs`. `make docs-check` runs it in verify-mode (no
// writes; exits non-zero if output would diverge from disk).
//
// Not meant to be invoked by end users of the guild binary.
package main

import (
	"bytes"
	"context"
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/spf13/cobra"
	"github.com/spf13/pflag"

	"github.com/google/jsonschema-go/jsonschema"
	sdkmcp "github.com/modelcontextprotocol/go-sdk/mcp"

	"github.com/mathomhaus/guild/internal/cli"
	"github.com/mathomhaus/guild/internal/mcp"
)

const autogenBanner = "<!-- auto-generated by cmd/docgen — run `make docs` to regenerate. DO NOT EDIT. -->"

func main() {
	var outDir string
	var check bool
	flag.StringVar(&outDir, "out", "docs/generated", "output directory")
	flag.BoolVar(&check, "check", false, "verify-only: fail non-zero if output would differ from disk")
	flag.Parse()

	cliDoc, err := renderCLI(cli.Root())
	if err != nil {
		fatal("render cli: %v", err)
	}

	mcpDoc, err := renderMCP()
	if err != nil {
		fatal("render mcp: %v", err)
	}

	readme := renderReadme()

	files := map[string]string{
		"README.md": readme,
		"cli.md":    cliDoc,
		"mcp.md":    mcpDoc,
	}

	if check {
		if diffs := checkFiles(outDir, files); len(diffs) > 0 {
			for _, d := range diffs {
				fmt.Fprintln(os.Stderr, d)
			}
			fmt.Fprintln(os.Stderr, "\ndocs are stale — run: make docs")
			os.Exit(1)
		}
		fmt.Println("✓ generated docs match disk")
		return
	}

	if err := writeFiles(outDir, files); err != nil {
		fatal("write: %v", err)
	}
	fmt.Printf("✓ wrote %d file(s) to %s\n", len(files), outDir)
}

// ---------------------------------------------------------------------------
// CLI doc rendering
// ---------------------------------------------------------------------------

func renderCLI(root *cobra.Command) (string, error) {
	var b strings.Builder
	b.WriteString(autogenBanner)
	b.WriteString("\n\n# guild CLI reference\n\n")
	b.WriteString("Every `guild <verb>` subcommand generated from the live cobra tree.\n\n")

	// Collect every command (depth-first), skipping hidden ones.
	var cmds []*cobra.Command
	var walk func(c *cobra.Command)
	walk = func(c *cobra.Command) {
		for _, sub := range c.Commands() {
			if sub.Hidden || sub.Name() == "help" {
				continue
			}
			cmds = append(cmds, sub)
			walk(sub)
		}
	}
	walk(root)
	sort.Slice(cmds, func(i, j int) bool {
		return cmds[i].CommandPath() < cmds[j].CommandPath()
	})

	// Table of contents.
	b.WriteString("## Verbs\n\n")
	for _, c := range cmds {
		path := c.CommandPath()
		anchor := strings.ReplaceAll(path, " ", "-")
		fmt.Fprintf(&b, "- [`%s`](#%s)", path, anchor)
		if c.Short != "" {
			fmt.Fprintf(&b, " — %s", c.Short)
		}
		b.WriteString("\n")
	}
	b.WriteString("\n")

	// Detail section per verb.
	for _, c := range cmds {
		renderCobraCmd(&b, c)
	}
	return b.String(), nil
}

func renderCobraCmd(b *strings.Builder, c *cobra.Command) {
	path := c.CommandPath()
	fmt.Fprintf(b, "## `%s`\n\n", path)
	if c.Short != "" {
		fmt.Fprintf(b, "%s\n\n", c.Short)
	}
	fmt.Fprintf(b, "**Usage**\n\n```\n%s\n```\n\n", c.UseLine())
	if len(c.Aliases) > 0 {
		fmt.Fprintf(b, "**Aliases:** %s\n\n", strings.Join(c.Aliases, ", "))
	}
	if c.Long != "" && c.Long != c.Short {
		fmt.Fprintf(b, "%s\n\n", c.Long)
	}

	var flags []*pflag.Flag
	c.LocalFlags().VisitAll(func(f *pflag.Flag) {
		if f.Hidden {
			return
		}
		flags = append(flags, f)
	})
	if len(flags) > 0 {
		sort.Slice(flags, func(i, j int) bool { return flags[i].Name < flags[j].Name })
		b.WriteString("**Flags**\n\n")
		b.WriteString("| flag | type | default | description |\n")
		b.WriteString("| --- | --- | --- | --- |\n")
		for _, f := range flags {
			short := ""
			if f.Shorthand != "" {
				short = ", `-" + f.Shorthand + "`"
			}
			def := f.DefValue
			if def == "" {
				def = "—"
			}
			fmt.Fprintf(b, "| `--%s`%s | %s | `%s` | %s |\n",
				f.Name, short, f.Value.Type(), def, oneLine(f.Usage))
		}
		b.WriteString("\n")
	}
}

